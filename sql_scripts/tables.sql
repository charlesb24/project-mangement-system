DROP TABLE IF EXISTS tasks;
DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS roles;
DROP TABLE IF EXISTS users_roles;

CREATE TABLE IF NOT EXISTS roles
(
    role_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name    VARCHAR(255)                            NOT NULL,
    CONSTRAINT pk_roles PRIMARY KEY (role_id)
);

CREATE TABLE IF NOT EXISTS tasks
(
    task_id        BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    parent_task_id BIGINT,
    level          INTEGER                                 NOT NULL,
    name           VARCHAR(255)                            NOT NULL,
    description    VARCHAR(255)                            NOT NULL,
    priority       INTEGER,
    status         SMALLINT,
    created_by     VARCHAR(255)                            NOT NULL,
    assigned_to    VARCHAR(255),
    created_at     TIMESTAMP WITHOUT TIME ZONE,
    updated_at     TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT pk_tasks PRIMARY KEY (task_id)
);

CREATE TABLE IF NOT EXISTS users
(
    user_id        BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    email          VARCHAR(255)                            NOT NULL,
    password       VARCHAR(255)                            NOT NULL,
    first_name     VARCHAR(255)                            NOT NULL,
    middle_name    VARCHAR(255),
    last_name      VARCHAR(255)                            NOT NULL,
    phone          VARCHAR(255),
    contact_method VARCHAR(255),
    manager_id     BIGINT,
    date_created   TIMESTAMP WITHOUT TIME ZONE,
    date_updated   TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT pk_users PRIMARY KEY (user_id)
);

CREATE TABLE IF NOT EXISTS users_roles
(
    role_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL
);

ALTER TABLE roles
    ADD CONSTRAINT uc_roles_name UNIQUE (name);

ALTER TABLE users
    ADD CONSTRAINT uc_users_email UNIQUE (email);

ALTER TABLE users
    ADD CONSTRAINT uc_users_phone UNIQUE (phone);

ALTER TABLE users_roles
    ADD CONSTRAINT fk_user FOREIGN KEY (role_id) REFERENCES roles (role_id);

ALTER TABLE users_roles
    ADD CONSTRAINT fk_role FOREIGN KEY (user_id) REFERENCES users (user_id);
